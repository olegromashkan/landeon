export const documentPages: ThesisPage[] = paginateThesisPages(documentSourcePages, pageAddenda);

function paginateThesisPages(
  sourcePages: ThesisPage[],
  addenda: Record<string, string[]>
): ThesisPage[] {
  const fixedPages = sourcePages.slice(0, frontMatterPages.length);
  const contentPages = sourcePages.slice(frontMatterPages.length);
  const result: ThesisPage[] = [...fixedPages];
  const pageCapacity = 28;
  let currentBlocks: TextBlock[] = [];
  let currentWeight = 0;
  let currentTitle = "Текст работы";
  let pageIndex = 1;

  const pushCurrentPage = () => {
    if (!currentBlocks.length) return;

    result.push({
      type: "content",
      navTitle: `${currentTitle} ${pageIndex}`,
      blocks: currentBlocks,
    });
    currentBlocks = [];
    currentWeight = 0;
    pageIndex += 1;
  };

  for (const page of contentPages) {
    if (page.type === "title") continue;

    const blocks = [
      ...page.blocks,
      ...(addenda[page.navTitle] ?? []).map((text): TextBlock => ({ type: "p", text })),
    ];

    for (const block of blocks) {
      const shouldStartMajorPage =
        block.type === "chapter" && currentBlocks.length > 0;
      const estimatedWeight = estimateBlockWeight(block);
      const exceedsPage =
        currentBlocks.length > 0 &&
        block.type !== "chapter" &&
        currentWeight + estimatedWeight > pageCapacity;

      if (shouldStartMajorPage || exceedsPage) {
        pushCurrentPage();
      }

      if (block.type === "chapter") {
        currentTitle = block.text;
      }

      currentBlocks.push(block);
      currentWeight += estimatedWeight;
    }
  }

  pushCurrentPage();

  return result;
}

function estimateBlockWeight(block: TextBlock): number {
  if (block.type === "chapter") return 4;
  if (block.type === "section") return 3;
  if (block.type === "p") return Math.max(2, Math.ceil(block.text.length / 115) + 1);
  if (block.type === "list") {
    return block.items.reduce((sum, item) => sum + Math.max(1, Math.ceil(item.length / 100)), 2);
  }
  if (block.type === "table") return Math.max(7, block.rows.length * 2 + 5);
  if (block.type === "figure") return block.src?.includes("/figure-d-") ? 18 : 13;
  return 4;
}
